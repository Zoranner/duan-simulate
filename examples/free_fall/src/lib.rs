//! free_fall 示例库
//!
//! 自由落体小球的完整仿真实现，展示 DUAN 仿真体系的核心用法。
//!
//! # 架构设计
//!
//! 本示例采用双域架构：
//!
//! - **运动域（motion）**：先执行，纯欧拉积分计算位置和速度，直接修改实体状态
//! - **碰撞域（collision）**：后执行（依赖运动域），检测碰撞、修正位置/速度、发出事件
//!
//! # 为什么分离运动域和碰撞域？
//!
//! 单域架构中，域计算发出碰撞事件，事件处理器修改实体速度，
//! 域的下一次迭代会读到被修改后的速度，导致误判和重复触发。
//!
//! 双域架构通过拓扑排序保证执行顺序：Motion.compute() 完成所有位置更新后，
//! Collision.compute() 才开始检测。碰撞域看到的是运动域的"干净"输出，
//! 不存在状态污染问题。

